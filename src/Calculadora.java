

public class Calculadora {
    public static void soma ( double numero1, double numero2){
        double resultado = numero1 + numero2;
        System.out.println( "a soma de" + numero1 + "mais" + numero2 + "é" + resultado);
    }
    public static void substração ( double numero1, double numero2){
        double resultado = numero1 - numero2;
        System.out.println( "a substração de" + numero1 + "menos" + numero2 + "é" + resultado);
    }
    public static void dicisão ( double numero1, double numero2){
        double resultado = numero1 / numero2;
        System.out.println( "a divisão de" + numero1 + "dividido" + numero2 + "é" + resultado);
    }
    public static void multilicação ( double numero1, double numero2){
        double resultado = numero1 * numero2;
        System.out.println( "a soma de" + numero1 + "multiplicado" + numero2 + "é" + resultado);
    }
}
